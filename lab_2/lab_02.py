# Variables
left_side=int(input("please enter first number: "))
right_side=int(input("please enter second number: "))
add=left_side + right_side
subtract=left_side - right_side
multiply=left_side * right_side
divide_float=left_side / right_side
exponent=left_side ** right_side
divide_integer=left_side // right_side
remainder=left_side % right_side

# Output
print(f"addition: {add}.")
print(f"subtraction: {subtract}.")
print(f"multiplication: {multiply}.")
print(f"float division: {divide_float}.")
print(f"exponential: {exponent}.")
print(f"integer division: {divide_integer}.")
print(f"mod: {remainder}.")